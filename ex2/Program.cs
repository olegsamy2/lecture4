void PrintImage(int[,] image) 
{ 
    for (int i = 0; i < image.GetLength(0); i++) 
    {
        { for (int j = 0; j < image.GetLength(1); j++) 
        {
            //  Console.WriteLine($"image[i,j]} ");
             if(image[i,j] == 0) Console.Write($" "); 
             // если точка совпадает с нулем, то на печать выводится "пробел"         
             else Console.Write($"+"); }  // иначе печатаем "+"   
        }
        Console.WriteLine();
    }
}    

void FillImage(int row, int col) 
{ 
    if (pic[row, col] == 0) // - если текущ.позиция 0, то его надо закрасить
    { 
        pic[row, col] = 1;  // закрашивание текущей позиции - 1
        FillImage(row - 1, col); // закрашивание объекта по нашему алгоритму 
        FillImage(row, col - 1); 
        FillImage(row + 1, col); 
        FillImage(row, col + 1);
     } 
}

